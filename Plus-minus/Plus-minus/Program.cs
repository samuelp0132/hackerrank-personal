    public static void plusMinus(List<int> arr)
    {
        var plusNumbers = arr.Count(e => e > 0);
        var minusNumbers = arr.Count(e => e < 0);
        var zeros = arr.Count(e => e == 0);

        Console.WriteLine(((decimal)plusNumbers) / arr.Count);
        Console.WriteLine(((decimal)minusNumbers) / arr.Count);
        Console.WriteLine(((decimal)zeros) / arr.Count);
    }

}

